import type { SecurityEvent } from '../types/security';

function hoursAgo(h: number, dayOffset = 0): string {
  const d = new Date();
  d.setDate(d.getDate() - dayOffset);
  d.setHours(d.getHours() - h, Math.floor(Math.random() * 50), 0, 0);
  return d.toISOString();
}

